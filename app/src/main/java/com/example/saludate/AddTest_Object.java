package com.example.saludate;

public class AddTest_Object {
    String done, date, name;

    public AddTest_Object() {
    }

    public AddTest_Object(String done, String date) {
        this.done = done;
        this.date = date;
    }

    public String getDone() {
        return done;
    }

    public void setDone(String done) {
        this.done = done;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
